def side_neighbors(p):
    i, j = p
    if int(i) != i:
        return {(i, j - 1), (i, j + 1)}
    else:
        return {(i - 1, j), (i + 1, j)}

def count_segments(sides):
    r = 0
    seen = set()
    while sides:
        side = list(sides)[0]
        seen.add(side)
        region = {side}
        frontier = {side}
        progress = True
        while progress:
            progress = False
            new_frontier = set()
            for s in frontier:
                sn = side_neighbors(s)
                for neighbor in sn:
                    if neighbor in sides:
                        if neighbor in seen or neighbor in region:
                            continue
                        progress = True
                        region.add(neighbor)
                        seen.add(neighbor)
                        new_frontier.add(neighbor)
            frontier = new_frontier
        r += 1
        sides -= region
    return r


